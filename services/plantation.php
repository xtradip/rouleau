<section style="background-color: var(--bg-off-white); padding: 4rem 0;">
    <div class="container">
        <div style="max-width: 800px; margin: 0 auto;">
            <h2 style="text-align: center; margin-bottom: 3rem;">Questions Fréquentes sur la Plantation d'Arbres</h2>

            <div style="margin-bottom: 2rem;">
                <h3 style="color: var(--primary-color); margin-bottom: 0.5rem;">Quels arbres sont les mieux adaptés au
                    climat de Sherbrooke ?</h3>
                <p>Pour l'Estrie, nous recommandons des espèces rustiques comme l'érable à sucre, le chêne rouge, le
                    bouleau blanc, le frêne (variétés résistantes à l'agrile), le tilleul et les conifères comme
                    l'épinette et le pin blanc. Ces arbres supportent bien nos hivers rigoureux et nos étés chauds.</p>
            </div>

            <div style="margin-bottom: 2rem;">
                <h3 style="color: var(--primary-color); margin-bottom: 0.5rem;">Quelle est la meilleure période pour
                    planter un arbre ?</h3>
                <p>Au Québec, les meilleures périodes sont le printemps (avril-mai) après le dégel et l'automne
                    (septembre-octobre) avant les gels. Le printemps permet à l'arbre de s'établir avant l'été, tandis
                    que l'automne favorise le développement racinaire avant l'hiver. Évitez de planter en pleine chaleur
                    estivale.</p>
            </div>

            <div style="margin-bottom: 2rem;">
                <h3 style="color: var(--primary-color); margin-bottom: 0.5rem;">À quelle distance de ma maison dois-je
                    planter un arbre ?</h3>
                <p>Cela dépend de la taille adulte de l'arbre. En règle générale : petits arbres (moins de 8m) à 3-4m de
                    la maison, arbres moyens (8-15m) à 5-7m, et grands arbres (plus de 15m) à minimum 8-10m. Il faut
                    aussi considérer les fils électriques, les fondations et les systèmes septiques.</p>
            </div>

            <div style="margin-bottom: 2rem;">
                <h3 style="color: var(--primary-color); margin-bottom: 0.5rem;">Combien coûte la plantation d'un arbre ?
                </h3>
                <p>Le coût varie selon la taille de l'arbre et le travail requis. Un petit arbre (2-3m) peut coûter 150$
                    à 300$ en main-d'œuvre. Un arbre de calibre moyen (4-6m) peut atteindre 400$ à 800$. Ces prix
                    n'incluent pas l'arbre lui-même. Nous pouvons vous aider à vous procurer les arbres ou travailler
                    avec ceux que vous avez choisis.</p>
            </div>

            <div style="margin-bottom: 2rem;">
                <h3 style="color: var(--primary-color); margin-bottom: 0.5rem;">Comment dois-je entretenir mon nouvel
                    arbre ?</h3>
                <p>Les deux premières années sont cruciales. Arrosez abondamment une fois par semaine (15-20L) en
                    l'absence de pluie. Maintenez le paillis autour du tronc (sans toucher l'écorce). Évitez la
                    fertilisation la première année. Nous vous fournissons un guide d'entretien détaillé adapté à votre
                    espèce d'arbre.</p>
            </div>

            <div style="margin-bottom: 2rem;">
                <h3 style="color: var(--primary-color); margin-bottom: 0.5rem;">Offrez-vous une garantie sur les arbres
                    plantés ?</h3>
                <p>Nous garantissons la qualité de notre travail de plantation. Cependant, la survie de l'arbre dépend
                    largement des soins post-plantation (arrosage, protection). Si vous suivez nos recommandations
                    d'entretien et que l'arbre ne survit pas, contactez-nous pour discuter de la situation. Certaines
                    pépinières offrent aussi leurs propres garanties sur les arbres.</p>
            </div>

