                <p>Cela dépend de la taille et de la densité. Un petit terrain résidentiel (5000-10000 pi²) peut être
                    déboisé en 1-2 jours. Un terrain d'un acre densément boisé peut prendre 3-5 jours. L'essouchement
                    ajoute du temps supplémentaire. Nous vous donnons un échéancier précis lors de la soumission.</p>
            </div>

