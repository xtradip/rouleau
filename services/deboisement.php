                    pour votre usage, l'évacuer complètement, ou le broyer en copeaux. Si le bois a une valeur
                    commerciale (gros billots de feuillus nobles), nous pouvons discuter d'arrangements qui pourraient
                    réduire vos coûts.</p>
            </div>

            <div style="margin-bottom: 2rem;">
                <h3 style="color: var(--primary-color); margin-bottom: 0.5rem;">L'essouchement est-il nécessaire ?</h3>
